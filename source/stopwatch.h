
#ifndef STOPWATCH_H
#define STOPWATCH_H

#include <stdbool.h>


/// @brief Stopwatch similar to C# class.

//---------------- Public API ----------------------//

/// Initialize the module.
/// @return 0=ok 1=high res not available.
int stopwatch_Init(void);

/// Reset the start time.
/// @return 0=ok 1=invalid stopwatch
int stopwatch_Reset(void);

/// How long since last stopwatch_Reset().
/// @return msec
double stopwatch_ElapsedMsec(void);

/// How long since stopwatch_Init().
/// @return msec
double stopwatch_TotalElapsedMsec(void);

#endif // STOPWATCH_H
